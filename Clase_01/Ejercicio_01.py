import math as m

def calculateSin(number:int) -> float:
    """
    Calculate the sinnus of a number
    """
    return m.sin(number)

def calculateCos(number:int) -> float:
    """
    Calculate the cos of a number
    """
    return m.cos(number)

def calculateTan(number:int) -> float:
    """
    Calculate the tan of a number
    """
    return m.tan(number)
